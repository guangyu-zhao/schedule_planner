  function getLang() {
    try {
      var stored = localStorage.getItem(STORAGE_KEY);
      if (stored && translations[stored]) return stored;
    } catch (e) {}
    return 'en';
  }

  function interpolate(str, params) {
    if (!params || typeof str !== 'string') return str;
    return str.replace(/\{(\w+)\}/g, function(_, key) {
      return params[key] != null ? String(params[key]) : '';
    });
  }

  window.I18n = {
    LANGUAGES: [
      { code: 'en', name: 'English', dir: 'ltr' },
      { code: 'zh-CN', name: '简体中文', dir: 'ltr' },
      { code: 'zh-TW', name: '繁體中文', dir: 'ltr' },
      { code: 'fr', name: 'Français', dir: 'ltr' },
      { code: 'de', name: 'Deutsch', dir: 'ltr' },
      { code: 'ja', name: '日本語', dir: 'ltr' },
      { code: 'ar', name: 'العربية', dir: 'rtl' },
      { code: 'he', name: 'עברית', dir: 'rtl' }
    ],

    t: function(key, params) {
      var lang = getLang();
      var dict = translations[lang] || translations.en;
      var val = dict[key];
      if (val === undefined) val = translations.en[key];
      if (val === undefined) return key;
      if (Array.isArray(val)) return val;
      return interpolate(val, params);
    },

    getLanguage: function() {
      return getLang();
    },

    setLanguage: function(lang) {
      if (!translations[lang]) return;
      try {
        localStorage.setItem(STORAGE_KEY, lang);
      } catch (e) {}
      var info = this.LANGUAGES.find(function(l) { return l.code === lang; });
      var dir = info ? info.dir : 'ltr';
      document.documentElement.dir = dir;
      document.documentElement.lang = lang;
      this.applyTranslations();
    },

    applyTranslations: function(root) {
      root = root || document;
      var self = this;
      [].forEach.call(root.querySelectorAll('[data-i18n]'), function(el) {
        el.textContent = self.t(el.getAttribute('data-i18n'));
      });
      [].forEach.call(root.querySelectorAll('[data-i18n-placeholder]'), function(el) {
        el.placeholder = self.t(el.getAttribute('data-i18n-placeholder'));
      });
      [].forEach.call(root.querySelectorAll('[data-i18n-title]'), function(el) {
        el.title = self.t(el.getAttribute('data-i18n-title'));
      });
    },

    translateError: function(errorMsg) {
      if (!errorMsg || typeof errorMsg !== 'string') return errorMsg;
      var key = CHINESE_ERROR_MAP[errorMsg.trim()];
      return key ? this.t(key) : errorMsg;
    },

    formatDate: function(date) {
      if (!(date instanceof Date)) date = new Date(date);
      var dayNames = this.t('cal.dayNames');
      var months = this.t('cal.months');
      if (!Array.isArray(dayNames)) dayNames = ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat'];
      if (!Array.isArray(months)) months = ['January', 'February', 'March', 'April', 'May', 'June', 'July', 'August', 'September', 'October', 'November', 'December'];
      var wd = dayNames[date.getDay()];
      var m = months[date.getMonth()];
      var d = date.getDate();
      var y = date.getFullYear();
      var lang = getLang();
      if (lang === 'zh-CN') return y + '年' + (date.getMonth() + 1) + '月' + d + '日 ' + wd;
      if (lang === 'zh-TW') return y + '年' + (date.getMonth() + 1) + '月' + d + '日 ' + wd;
      if (lang === 'ja') return y + '年' + (date.getMonth() + 1) + '月' + d + '日 ' + wd;
      if (lang === 'de') return d + '. ' + m + ' ' + y + ' ' + wd;
      if (lang === 'ar' || lang === 'he') return d + ' ' + m + ' ' + y + ' ' + wd;
      return m + ' ' + d + ', ' + y + ' ' + wd;
    },

    formatMonth: function(year, month) {
      var months = this.t('cal.months');
      if (!Array.isArray(months)) months = ['January', 'February', 'March', 'April', 'May', 'June', 'July', 'August', 'September', 'October', 'November', 'December'];
      var m = months[month];
      return this.t('cal.monthFormat', { month: m, year: year });
    }
  };

